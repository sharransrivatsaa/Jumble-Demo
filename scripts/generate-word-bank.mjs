#!/usr/bin/env node
/**
 * Fetches the public Wordle answer list and writes word bank + difficulty tiers.
 * Source: https://gist.github.com/cfreshman/a03ef2cba789d8cf00c08f767e0fad7b
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { definitionToClue } from "./emoji-utils.mjs";

const SOURCE_URL =
  "https://gist.githubusercontent.com/cfreshman/a03ef2cba789d8cf00c08f767e0fad7b/raw/wordle-answers-alphabetical.txt";

const CLUE_WORDS = new Set([
  "APPLE", "BRAIN", "CHAIR", "DREAM", "EAGLE", "FLAME", "GRAPE", "HOUSE",
  "IVORY", "JOKER", "KNIFE", "LEMON", "MAGIC", "NIGHT", "OCEAN", "PIANO",
  "QUEEN", "RIVER", "STORM", "TIGER", "ULTRA", "VIVID", "WHALE", "XENON",
  "YACHT", "ZEBRA", "BREAD", "CLOUD", "DANCE", "EARTH", "FROST", "GHOST",
  "HEART", "IMAGE", "JUICE", "LIGHT", "MUSIC", "NOVEL", "OLIVE", "PEARL",
  "QUILT", "RADIO", "SUGAR", "TRAIN", "UNITY", "VOICE", "WATER", "YOUTH",
  "PLANT", "STONE",
]);

function difficultyFor(word) {
  if (CLUE_WORDS.has(word)) return "easy";

  const vowels = (word.match(/[AEIOU]/g) ?? []).length;
  if (/[QXZJ]/.test(word)) return "hard";
  if (vowels <= 1) return "hard";

  const commonOnly = /^[AEIOURSTLNMDCBPGRHF]+$/;
  let score = 0;
  if (commonOnly.test(word)) score += 2;
  if (vowels >= 2) score += 1;
  if (vowels >= 3) score += 1;
  if (/[KVWY]/.test(word)) score -= 1;

  if (score >= 4) return "easy";
  if (score <= 2) return "hard";
  return "medium";
}

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataDir = join(__dirname, "../src/data");

const response = await fetch(SOURCE_URL);
if (!response.ok) {
  throw new Error(`Failed to fetch word list: ${response.status}`);
}

const words = (await response.text())
  .split("\n")
  .map((line) => line.trim().toUpperCase())
  .filter((word) => word.length === 5 && /^[A-Z]+$/.test(word));

const unique = [...new Set(words)].sort();

const tiers = { easy: [], medium: [], hard: [] };
for (const word of unique) {
  tiers[difficultyFor(word)].push(word);
}

mkdirSync(dataDir, { recursive: true });

writeFileSync(
  join(dataDir, "word-bank.ts"),
  `// Auto-generated by scripts/generate-word-bank.mjs — do not edit by hand.
// Source: ${SOURCE_URL}
export const WORD_BANK: string[] = ${JSON.stringify(unique, null, 2)};
`,
  "utf8",
);

writeFileSync(
  join(dataDir, "word-tiers.ts"),
  `// Auto-generated by scripts/generate-word-bank.mjs — do not edit by hand.
import type { Difficulty } from "../lib/words";

export const WORDS_BY_DIFFICULTY: Record<Difficulty, string[]> = ${JSON.stringify(tiers, null, 2)};
`,
  "utf8",
);

console.log(`Wrote ${unique.length} words to src/data/word-bank.ts`);
console.log(
  `Wrote tiers — easy: ${tiers.easy.length}, medium: ${tiers.medium.length}, hard: ${tiers.hard.length}`,
);

// --- Plain-English clues for all words (from dictionary definitions) ---

const clueWords = unique.filter((word) => !CLUE_WORDS.has(word));
const textClues = {};
const delay = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

console.log(`Generating text clues for ${clueWords.length} words…`);

for (let i = 0; i < clueWords.length; i += 1) {
  const word = clueWords[i];
  if (i % 100 === 0) {
    console.log(`  ${i}/${clueWords.length}…`);
  }

  let clue = "";
  try {
    const res = await fetch(
      `https://api.datamuse.com/words?sp=${word.toLowerCase()}&md=d&max=1`,
    );
    const data = await res.json();
    const definition = data[0]?.defs
      ?.map((entry) => entry.split("\t").slice(1).join(" "))
      .join(" | ") ?? "";

    clue = definitionToClue(definition, word);
  } catch {
    clue = `A five-letter word starting with "${word[0]}".`;
  }

  textClues[word] = clue;
  await delay(25);
}

writeFileSync(
  join(dataDir, "text-clues.ts"),
  `// Auto-generated by scripts/generate-word-bank.mjs — do not edit by hand.
export const TEXT_CLUES: Record<string, string> = ${JSON.stringify(textClues, null, 2)};
`,
  "utf8",
);

console.log(`Wrote ${Object.keys(textClues).length} text clues to src/data/text-clues.ts`);
